                        <h3>Our Vission...</h3>

                        Our Vision is to see individuals grow in their relationship with the Lord, after they dedicate their lives to him</p>

                        <h3>Our Philosophy...</h3>
                        <p>Building lives for the Kingdom of God. (Hebrews 12:28)</p>
                        <h3>Our Purpose…</h3>
                        <p>To win the unchurched and to assimilate them into a committed body of spirit filled believers. To equip those believers under the leadership of qualified ministers, enabling them to fulfill their God-given purpose. To achieve a level of growth and maturity in these believers that they may influence the community, the state and the world. (Matthew 28:12)Our Primary Vision To build the life of every child of God to it’s fullest capacity of service for the kingdom in Fellowship, Education & Outreach.</p>

