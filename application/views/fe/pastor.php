                            <p>In 2007, after completing High School, he founded the Evangelistic Ministry which went all over Kenya preaching the gospel of the good news. in 2009, he became the secretary of Nazareth Gospel Mission Nairobi. In 2010, he moved to work at Narok. In that Year, he organized an evangelism meeting and at the end of it, a church had been born. He served as the pastor of that church. He served for six months but then he was recalled back to Nairobi. He joined his earlier church as a church elder, a position he held for six years upto to 2016.From 2014, God had been calling him to pastor a local, New Testament Church and in the end of 2016, he took up the call.</p>

                            <h3>Ministry life</h3>

                            <p>The Mwicigi family have served at different capacities in Nazareth Gospel Mission under the leadership of Pastor john Kariuki. On August 15th, 2016, The Good News Fellowship was formed and in 2017, it was chartered as an independent, local, New Testament Church with Brother Moses as it’s Senior Pastor. In October 2016, brother Moses resigned his service at Nazareth Gospel Church and committed his full-time energies to reaching the unreached with the message of the good news.</p>
