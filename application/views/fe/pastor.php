                        <div class="post_desc clearfix" style="text-align: justify;""> 
                            <p><span style="font-size: 15px; color: #31870E;font-weight: bolder">Pastor Moses Mwicigi is the senior Pastor</span> at The Good News Fellowship International.  He currently organises and performs most responsibilities around the ministry.</p>
                            <h3>Background</h3>

                            <p>Pastor Moses was born in Nakuru. He moved to Nairobi in the year 2007. During his childhood and teenage years, Moses was raised in a pentecostal Church. Upon graduating high school, he went to college at Kenyatta University and earned a 4 year Bachelor’s Degree in Information and Computer Technology.</p>

                            <p>Pastor Moses is the second born in a family of nine. In his early life, he lived in so much poverty which cast a big cloud of hopelessness and hate. in Novemebr 14,2003 he tried to commit suicide. But by God's devine intervention, He resqued him from the grip of death. In 2004, he gave his life back to Christ. in 2005 and 2006, he served as the Christian Union Chairman at Njoro High School.</p>

                            
